class DEConv(nn.Module):
    def __init__(self, in_channel, out_channel):
        super(DEConv, self).__init__()
        #
        self.conv1_1 = Conv2d_cd(in_channel, out_channel, 3, bias=True)
        self.conv1_2 = Conv2d_hd(in_channel, out_channel, 3, bias=True)
        self.conv1_3 = Conv2d_vd(in_channel, out_channel, 3, bias=True)
        self.conv1_4 = Conv2d_ad(in_channel, out_channel, 3, bias=True)
        self.conv1_5 = nn.Conv2d(in_channel, out_channel, 3, padding=1, bias=True)

    def forward(self, x):
        w1, b1 = self.conv1_1.get_weight()
        w2, b2 = self.conv1_2.get_weight()
        w3, b3 = self.conv1_3.get_weight()
        w4, b4 = self.conv1_4.get_weight()
        w5, b5 = self.conv1_5.weight, self.conv1_5.bias

        w = w1 + w2 + w3 + w4 + w5
        b = b1 + b2 + b3 + b4 + b5
        res = nn.functional.conv2d(input=x, weight=w, bias=b, stride=1, padding=1, groups=1)

        return res

